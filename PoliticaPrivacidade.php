                <h4>O que é a LGPD?</h3>
                <p>A Lei Geral de Proteção de Dados (LGPD) é a legislação brasileira que tem por finalidade proteger os direitos fundamentais de liberdade, privacidade e o livre desenvolvimento da personalidade da pessoa natural.</p>
                <p>Dados Pessoais são informações relativas a uma pessoa física que tenham a capacidade de tornar tal pessoa identificada ou identificável. Alguns exemplos de dados pessoais são nome, e-mail, CPF, IP, data de nascimento etc.</p>
            </div>
            <div class="divisaopolitica">
                <h4>O que são dados pessoais sensíveis?</h3>
                <p>São dados pessoais sensíveis, de acordo com a LGPD, aqueles que podem causar algum tipo de discriminação e, por isso, exigem um maior nível de proteção.</p>
                <p>Alguns deles são: origem racial ou étnica, convicção religiosa, opinião polícia, filiação a sindicato ou organização de caráter religioso, filosófico ou político, dado refere à saúde ou à vida sexual, dado genético ou biométrico, desde que sejam vinculados a uma pessoa natural.</p>
